#define _CRT_SECURE_NO_WARNINGS // 전처리기 세팅

// 필요시 추가 헤더: <stdlib.h>, <string.h>, <math.h> 등
#include <stdio.h>
#include <string.h>

// 함수 선언

int main(void)
{
    // 변수 선언 및 초기화
    int a = 10;
    int b = 10;

    // 데이터 준비 및 입력
    ++a;
    --b;

    // 로직 처리

    // 결과 출력
    printf("a : %d\n", a);
    printf("b : %d\n", b);

    // 함수종료
    return 0;
}

