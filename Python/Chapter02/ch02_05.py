# 문자열 연산자
# 문자 선택 연산자 (인덱싱) : []

print("문자 선택 연산자에 대해 알아볼까요?")
print("안녕하세요"[0])#안
print("안녕하세요"[1])#녕
print("안녕하세요"[2])#하
print("안녕하세요"[3])#세
print("안녕하세요"[4])#요

print("안녕하세요"[-1])#요
print("안녕하세요"[-2])#세
print("안녕하세요"[-3])#하
print("안녕하세요"[-4])#녕
print("안녕하세요"[-5])#안

str1 = "안녕하세요"
print(str1[0:3])#안녕하
print(str1[2:])#하세요
print(str1[:])#안녕하세요